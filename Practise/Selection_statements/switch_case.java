public class switch_case{
   public static void main(String[] args){
	int option=2;
	switch (option){
	   case 1:
		System.out.println("one");
		break;
	   case 2:
		System.out.println("two");
		break;
	   case 3:
		System.out.println("three");
		break;
	   default:
		System.out.println("!!!!!!");
	}
   }
}